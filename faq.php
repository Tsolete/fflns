<h3>¿Por qué los enemigos me golpean y me golpean cuando estoy en el aire hasta quitarme toda la barra de vida "sin que yo pueda hacer nada"?<h3>

<p>Porque eres un matraco incapaz de leerte el <a href="index.php?seccion=howto" style="color:black">How to Play</a>. En él se explica que una vez en el aire se debe pulsar arriba seguido del botón de ataque para realizar una recuperación aérea que da inmunidad. (no funciona contra ciertos ataques de Akuma/Gouki que no permites recuperarse, con el resto de enemigos y jefes funciona con todos).</p>

<h3>¿Por qué a veces me sale Akuma en vez de Bison en el modo Arcade de enemigo final?</h3>

<p>Por los puntos obtenidos durante la partida, hasta el momento en que muere Sagat. Para enfentarse a Akuma hay que obtener 450.000 Puntos mas 115.000 puntos por cada jugador extra en todas las dificultades, excepto en el modo Musou. Es decir:
	<ul>
		<li>1P: 450.000</li>
		<li>2P:(la suma de los puntos de los jugadores) 555.000</li>
		<li>3P:(la suma de los puntos de los jugadores) 670.000</li>
		<li>4P:(la suma de los puntos de los jugadores) 785.000</li>
	</ul>
</p>
<br>
<p>En el modo Musou (Masivo) estas puntuaciones cambian siendo los valores 690.000 puntos mas 35.000 por cada jugador extra. Es decir: 
	<ul>
