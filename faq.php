</p>

<h3>¿Qué es eso del Juggling y porque a veces los jefes caen al suelo sin que pueda golpearlos más mientras se oye una sugerente voz femenina?</h3>

<p>El Juggling (Malabares en castellano) es una variable del juego que nos va a permitir golpear a los enemigos un determinado numero de veces sin que caigan al suelo.</p> 

<p>Este valor es en la mayoría de jefes y enemigos de 50 puntos (en algunos jefes es menor). Cada golpe que propinamos a un enemigo cuando esta en el aire le quita 2 puntos de esa variable, es decir una vez que un enemigo esta en el aire puede recibir 25 golpes, en ese momento caera al suelo sin que podamos golpearlo, y si se trata de un jefe se oirá la voz que nos avisa de que no podemos golpear más "NO MORE JUGGLING!!!!".</p>

<p>Las excepciones son varias, las magias que gastan 20 de mana solo gastan 2 puntos de Juggling aunque sean multihit y golpeen varias veces  a los enemigos, hay algunas magias que llegan a gastar 3, normalmente suele ser una magia por personaje y suele ser la magia que da mayor inmunidad y que es mas dañina para los enemigos (ejemplo los Shoriukens).</p>

<p>Otra excepción son los ultimates o supermagias, estos movimientos no gastan nada de juggling es decir da igual las veces que golpeen al enemigo que este no caerá al suelo.</p>

<p>Por ultimo, hay golpes que de un solo impacto gastan todo el juggling de los enemigos golpeados, estos son los golpes desesperados o dos botones (boton de ataque mas boton de salto simultaneamente), AIR RECOVERY/RECUPERACION AEREA (Mando arriba continuado de boton de ataque cuando estamos en el aire tras ser derribados) y RISE ATTACK/ATAQUE LEVANTANDONOS (Mando arriba continuado de boton de ataque cuando estamos en el suelo tras haber sido derribados). Cuando uno de estos tres tipos de golpe impactan a un enemigo este cae al suelo sin posibilidad de poder seguir combeandolo.</p>

<h3>Tengo que dejar de jugar,¿hay alguna forma de guardar mi progreso?</h3>

<p>Openbor permite cargar partidas anteriores. Solo tienes que ir a "load game" y recuperar tu última partida. El juego volverá a empezar desde el principio de la última fase dónde lo dejaste.</p>

<h3>¿Para qué sirve el botón de parry? </h3>

<p>Tiene varias utilidades, su función más usada es la de bloquear ataques pero también podemos agarrar a los enemigos en el aire (si el personaje en concreto lo tiene) y realizar algunas magias. (ver <a href="index.php?seccion=protagonistas" style="color:black">move list</a>)</p>

<h3>Algunas veces se escucha un voz que dice "super combo!" y otras veces "ultra combo!" ¿Qué significa?</h3> 

<p>En el caso de que escuches "super combo!" quiere decir que has alcanzado los 40 golpes en el contador de combos de abajo a la izquierda. Por lograrlo el juego te recompensa con 250 puntos. Si escuchas "ultra combo!" es que has llegado a 80 golpes y el juego te recompensa con 500 puntos.</p>

<h3>¿Los especiales o ultimate moves de cada personaje quitan la misma vida?</h3>

<p>Depende. Lo normal es que haya una relación entre el alcance y el daño. Es decir, si un especial barre toda la pantalla de enemigos y el otro solo golpea a un enemigo este último quitará más vida.<7p>

