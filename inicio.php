<h3>Final Fight LNS</h3>
<p><img alt="personajes" src="imagenes/full.png" align="right">
Final Fight LNS es un beat'em up que mezcla conceptos y movimientos tanto de este género como del
género de lucha. Cuenta con un revolucionario y novedoso sistema de combeo basado en el "cancelling", el cual nos permite enlazar movimientos especiales dentro de otros puediendo así
realizar un número muy elevado de golpes.
El juego además está diseñado para que su dificultad disminuya considerablemente a medida que vamos
aprendiendo y ejecutando este sistema, por lo que la curva de aprendizaje es pronunciada. En otras
palabras, el juego nos va a recompensar a medida que aprendamos a inflingir el máximo daño posible
a los objetivos.</p>
<br>
<p>El plantel de personajes jugables consta de 46 personajes combinando luchadores de varias de las sagas más exitosas de Capcom tales como: Final Fight, Street Fighter, Cadillacs and Dinosaurs, Captain 
Commando, Alien vs Predator, Battle Circuit, Darkstalkers y Strider.
A pesar del amplio repertorio cada personaje está balanceado y arreglado para que la experiencia
juegable sea óptima elijamos el personaje que elijamos y tengamos posibilidades de éxito con cualquiera de los 46.</p>
<br>
<p>En el apartado musical hemos seleccionado temas que van desde los temas clásicos hasta versiones (o covers) de esos temas hechas por gente con muchísimo talento para la música, pasando por temas que nada tienen que ver con el mundo del videojuego como canciones "synthwave". 
Cada parte de cada fase tiene un tema diferente, haciendo así que la experiencia al jugarlo se haga menos monótona y disfrutable para el jugador.</p>
<br>
<p>El cuanto al control, el juego consta de un mando direccional y 3 botones para realizar todas las acciones que se nos permiten, Tanto movimientos básicos como movimientos especiales.
En el apartado de movimientos básicos vamos a poder realizar bastantes acciones con cada luchador,
tanto ofensivas como defensivas o evasivas. Por lo que la sensación de libertad a la hora del control es muy satisfactoria. Entre estos movimientos tendremos: correr, movernos verticalmente mientras corremos, saltar,saltar hacia atrás, saltar verticalmente, bloquear ataques y realizar contraataques.</p>
<br>
<p>En los menús vamos a disponer de diferentes modos de juego , cada uno con diferentes modos de dificultad  por lo que podremos siempre elegir una opción que se ajuste a nuestro nivel y a nuestro
tiempo. 
Aparte de la partida convencional disponemos de varios modos como son: Boss Rush, Volleybred,Versus Mode y Bonus Mode. 
Además disponemos de un excelente modo de entrenamiento para poder probar todo lo que queramos de cualquier personaje.</p>
<br>
<p>En el desarrollo de fases hemos querido mantener la esencia del primer Final Fight, de tal modo que
la acción se desarrolla en Metro City. Por eso hemos recreado los escenarios más característicos
de esta franquicia y los hemos mezclado con otros que a nuestro juicio, mantienen su esencia y aportan un aire fresco a estos.</p>
<br>
<p>Final Fight LNS no cuenta con un sistema propio para jugar on-line, pero a dia de hoy este inconveniente se subsana con programas como Parsec, el cual recomendamos encarecidamente para poder
jugar Final Fight en cooperativo sin problema alguno.</p>

